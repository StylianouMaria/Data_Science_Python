#Data Visualisation with Python - II

#Scatterplot or x-y graph: 
'''
• Able to identify trends and relationships between 2 variables
• Data characterised by numerical values, 2 variables
• Not useful for categorical data, or time series
'''


#Scatterplot matrix: 
'''
• Assist to determine relationships between multiple variables
• Best for data characterised by numerical and multiple variables
• Not useful for time series
'''


#Double axis chart or Dual-axis chart: 
'''
• Can be bar chart owith line chart or multiple line on visualisation
• Can be used for time series, discrete or contrinuous data
• But can be overwhelming
'''